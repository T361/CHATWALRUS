#!/usr/bin/env node
/**
 * Merge duplicate company records.
 * Moves all learner/enrollment/progress data from SOURCE slug → TARGET slug,
 * then deactivates the source company.
 *
 * Usage:
 *   node scripts/merge-duplicate-companies.mjs
 */

import { createClient } from '@supabase/supabase-js';

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
  console.error('❌ Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY');
  process.exit(1);
}

global.WebSocket = class WebSocket {};
const db = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, {
  auth: { persistSession: false },
});

// Pairs: { source: slug with the DATA, target: slug that should be canonical }
const MERGE_PAIRS = [
  { source: 'omnilux',      target: 'omnilux-led',    reason: 'OmniLux LED is the Thinkific group name' },
  { source: 'robin-hood',   target: 'robinhood',      reason: 'Robinhood is the correct company name' },
  { source: 'topo',         target: 'topo-designs',   reason: 'Topo Designs is the Thinkific group name' },
  { source: 'soapbox',      target: 'soapbox-soaps',  reason: 'Soapbox Soaps is the Thinkific group name' },
  { source: 'smart-sweets', target: 'smartsweets',    reason: 'SmartSweets is the newer canonical name' },
];

// Tables with a company_id column to reassign
const COMPANY_ID_TABLES = [
  'learners',
  'enrollments',
  'lesson_progress',
  'zoom_attendance',
  'zoom_sessions',
  'orders',
  'assignments',
  'surveys',
  'milestone_checks',
  'learner_directory_rollups',
  'daily_snapshots',
  'leaderboard_snapshots',
];

// Tables to delete from source (they'll be regenerated by next sync/rollup)
const DELETE_TABLES = [
  'company_summary_rollups',
  'company_weekly_rollups',
  'weekly_company_rollups',
];

async function getCompany(slug) {
  const { data, error } = await db
    .from('companies')
    .select('id, name, slug, is_active')
    .eq('slug', slug)
    .maybeSingle();
  if (error) throw error;
  return data;
}

async function mergeCompany(sourceSlug, targetSlug, reason) {
  console.log(`\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━`);
  console.log(`Merging: "${sourceSlug}" → "${targetSlug}"`);
  console.log(`Reason:  ${reason}`);

  const source = await getCompany(sourceSlug);
  const target = await getCompany(targetSlug);

  if (!source) { console.log(`  ⚠️  Source "${sourceSlug}" not found — skipping`); return; }
  if (!target) { console.log(`  ⚠️  Target "${targetSlug}" not found — skipping`); return; }

  console.log(`  Source: ${source.name} (${source.id}) — active: ${source.is_active}`);
  console.log(`  Target: ${target.name} (${target.id}) — active: ${target.is_active}`);

  // Reassign each table
  for (const table of COMPANY_ID_TABLES) {
    try {
      const { count, error } = await db
        .from(table)
        .update({ company_id: target.id })
        .eq('company_id', source.id)
        .select('id', { count: 'exact', head: true });

      if (error) {
        // Table might not have company_id — skip silently
        if (error.code === 'PGRST116' || error.message?.includes('column') || error.message?.includes('does not exist')) {
          continue;
        }
        console.log(`  ⚠️  ${table}: ${error.message}`);
      } else {
        if ((count ?? 0) > 0) console.log(`  ✅ ${table}: reassigned ${count} rows`);
      }
    } catch {
      // Table doesn't exist — skip
    }
  }

  // Delete stale rollup rows for source (regenerated by cron)
  for (const table of DELETE_TABLES) {
    try {
      const { error } = await db.from(table).delete().eq('company_id', source.id);
      if (error && !error.message?.includes('does not exist')) {
        console.log(`  ⚠️  ${table} delete: ${error.message}`);
      }
    } catch {
      // skip
    }
  }

  // Deactivate source company
  const { error: deactivateError } = await db
    .from('companies')
    .update({ is_active: false })
    .eq('id', source.id);

  if (deactivateError) {
    console.log(`  ❌ Failed to deactivate source: ${deactivateError.message}`);
  } else {
    console.log(`  ✅ "${source.name}" deactivated`);
  }

  // Also deactivate any learners still remaining on source (shouldn't be any, but safety)
  await db.from('learners').update({ is_active: false }).eq('company_id', source.id);

  console.log(`  ✅ Merge complete: data now under "${target.name}"`);
}

async function main() {
  console.log('🔀 Company Merge Script');
  console.log(`Processing ${MERGE_PAIRS.length} merge pairs...\n`);

  for (const pair of MERGE_PAIRS) {
    await mergeCompany(pair.source, pair.target, pair.reason);
  }

  console.log('\n✅ All merges complete.');
  console.log('Next: run Backfill Learner Rollups + Backfill Weekly Rollups from Admin Settings to regenerate aggregates.');
}

main().catch((err) => {
  console.error('💥 Script failed:', err);
  process.exit(1);
});
